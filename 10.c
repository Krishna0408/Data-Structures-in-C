#include<stdio.h>
int main(){
int n,i,j;
scanf("%d",&n);
for(i=1;i<=n;i++){
        for(j=1;i<=i;j--){
            printf(" ");
        }
        for(j=1;i<=i;j++){
        printf("%d", j);
}
printf("\n");
}
for(i=1;i<=n;i++) {
        printf(" ");
}
for(j=1;i<=i;j++){
        printf("%d", j);
}
printf("\n");
}
